x = int(input("Настругай первое целое число: "))
y = int(input("Настругай второе целое число: "))

print("Числа x и y:", x, y)

сума = x + y
print("Сума чисел x та y:", сума)

поділ = x // y
print("Цілочисельний поділ x на y:", поділ)


решта = x % y
print("Залишок від ділення x на y:", решта)

ступінь = x ** y
print("Число x в степені y:", ступінь)
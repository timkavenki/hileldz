'''2. Користувач вводить число `X`, яке має два знаки після десяткової точки.
 - Виведіть його дрібну частину.
  - Виведіть першу цифру після десяткової точки.'''''


x = float(input("Введіть число з двома знаками після десяткової точки: "))
fractional_part = x % 1
print("Дрібна частина числа:", fractional_part)
first_digit_after_decimal = int(x * 10) % 10
print("Перша цифра після десяткової точки:", first_digit_after_decimal)
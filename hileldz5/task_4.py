'''Даний перелік випадкових цілих чисел. Замініть усі непарні числа списку нулями. І виведете їхню кількість.'''


numbers = [23, 45, 12, 67, 89, 34, 17, 66, 91, 55]

count = 0
for i in range(len(numbers)):
    if numbers[i] % 2 != 0:
        numbers[i] = 0
        count += 1

print("Кількість замінених чисел:", count)
print("Змінений список чисел:", numbers)